import UIKit
import Foundation

extension UIColor {
  
  open class var salmon: UIColor {
    return UIColor(red: 255/255, green: 45/255, blue: 85/255, alpha: 1)
  }
  
  open class var mutedSalmon: UIColor {
    return UIColor(red: 255/255, green: 45/255, blue: 85/255, alpha: 0.8)
  }
  
  open class var veryLightGray: UIColor {
    return UIColor(white: 0.9, alpha: 1.0)
  }
}
